//  redefine coordinates to go where the shower is maximum
  double xn = x+ux*showmax;
  if(TMath::Abs(xn)<hx) {
    x = xn;
  } else {
    if(xn>0.) x = hx;
    if(xn<0.) x =-hx;
  } 
  double yn=y+uy*showmax;
  if(TMath::Abs(yn)<hy) {
    y = yn;
  } else {
    if(yn>0.) y = hy;
    if(yn<0.) y =-hy;
  } 
  /*if(current.evnt%steer.nevfreq == 0) {
     printf(" coordinates of maximum shower: xn = %f, yn = %f\n",xn,yn);
     printf(" redefinition of coordinates: x = %f, y = %f\n",x,y);
  }*/

//  add statistical fluctuations on the phe number
double res = TMath::Sqrt((eneres1*eneres1/energy)+(eneres2*eneres2));
double fluctpc = res*TMath::Sqrt2();
double fluct = fluctpc*phetot/100.;
double phesum = phetot*notunif;
double phec = 2.0 * phetot - phesum;
phesum = gRandom->Gaus(phesum, fluct);
if(phesum < 0.0) phesum = 0.0;
phec = gRandom->Gaus(phec,fluct);
if(phec < 0.0) phec = 0.0;
/*if(current.evnt%steer.nevfreq==0){
  printf(" > pheZP: res = %1.2f", res);
  printf("\t phetot = %f - fluct = %f\n", phetot, fluct);
  //printf("\t ifibx = %d - ifiby = %d\n", ifibx, ifiby);
  //printf("\t dxtofib  = %f - dytofib = %f - dtofib = %f\n", dxtofib, dytofib, dtofib);
  printf("\t phe on pmc = %f, sumpmq = %f\n", phec, phesum);
}*/
//define vector
bzp(0) = par_t1[0]+par_t1[1]*TMath::ATan(par_t1[2]*(x+par_t1[3]));
bzp(1) = par_t1t2[0]+par_t1t2[1]*TMath::ATan(par_t1t2[2]*(x+par_t1t2[3]));
