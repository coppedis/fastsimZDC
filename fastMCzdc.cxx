              printf(" probability to interact in pipe for protons = %f\n", r);
              printf(" Released/true energy in ZP when proton interacts in pipe = %f\n", er);
              if(sflFlag == 1) printf(" Released energy (GEANT3) = %f\n", RelEneG3); 
