void rmv_fr(int ipspec, int inspec, int &npf, int &nnf){
//    double bmax_cs=17.5;
    double avfn=0.;
    double avfp=0.;
    static double c_pb_n[4] = {2.6453, -1.4627,  0.92266, -0.46428E-01};
    static double c_pb_p[4] = {0.55181, 0.17406, 0.19849, -0.12357E-01};
    static double c_xe_n[4] = {2.786,  -1.4557,  0.70712, -0.38864e-01};
    static double c_xe_p[4] = {0.6342,  0.16623, 0.15300, -0.10876e-01};
    static double c_ox_n[6] = {1.439,0.361,-0.581,0.253,-0.0354,0.001535};
    static double c_ox_p[6] = {1.439,0.361,-0.581,0.253,-0.0354,0.001535};
    static double errpc = 0.15;
    
/*
    printf("inside rmv_fr: b = %f\n",ev1.b);
    printf("inside rmv_fr: inspec = %d   -  ipspec = %d\n",inspec,ipspec);
    printf("Aproj = %f , Zproj = %f\n",steer.Aproj,steer.Zproj);
*/
    if(inspec>0) {
      if(steer.Aproj==208.){
        avfn=c_pb_n[0]+c_pb_n[1]*ev1.b+c_pb_n[2]*ev1.b*ev1.b+c_pb_n[3]*ev1.b*ev1.b*ev1.b;
      } else if(steer.Aproj==129.){
        avfn=c_xe_n[0]+c_xe_n[1]*ev1.b+c_xe_n[2]*ev1.b*ev1.b+c_xe_n[3]*ev1.b*ev1.b*ev1.b;
      } else if(steer.Aproj==16.){
        avfn=c_ox_n[0]+c_ox_n[1]*ev1.b+c_ox_n[2]*ev1.b*ev1.b+c_ox_n[3]*ev1.b*ev1.b*ev1.b+c_ox_n[4]*ev1.b*ev1.b*ev1.b*ev1.b+c_ox_n[5]*ev1.b*ev1.b*ev1.b*ev1.b*ev1.b;
      }
      double dfn=errpc*avfn;
      double fn=gRandom->Gaus(avfn,dfn);
//      printf("avfn = %f - dfn = %f - fn =%f\n",avfn,dfn,fn);
      if(fn<0.0) fn=0.0;
      nnf=TMath::Nint(fn);
      if(nnf>inspec) nnf=inspec;
//      printf("nnf =%d\n",nnf);
    }
    else {
      nnf=0;
//      printf("nnf =%d\n",nnf);
    }
    if(ipspec>0) {
      if(steer.Aproj==208.){
        avfp=c_pb_p[0]+c_pb_p[1]*ev1.b+c_pb_p[2]*ev1.b*ev1.b+c_pb_p[3]*ev1.b*ev1.b*ev1.b;
      } else if(steer.Aproj==129.){
        avfp=c_xe_p[0]+c_xe_p[1]*ev1.b+c_xe_p[2]*ev1.b*ev1.b+c_xe_p[3]*ev1.b*ev1.b*ev1.b;
      } else if(steer.Aproj==16.){
        avfp=c_ox_p[0]+c_ox_p[1]*ev1.b+c_ox_p[2]*ev1.b*ev1.b+c_ox_p[3]*ev1.b*ev1.b*ev1.b+c_ox_p[4]*ev1.b*ev1.b*ev1.b*ev1.b+c_ox_p[5]*ev1.b*ev1.b*ev1.b*ev1.b*ev1.b;
      }
      double dfp=errpc*avfp;
      double fp=gRandom->Gaus(avfp,dfp);
//      printf("avfp = %f - dfp = %f - fp =%f\n",avfp,dfp,fp);
      if(fp<0.0) fp=0.0;
      npf=TMath::Nint(fp);
      if(npf>ipspec) npf=ipspec;
//      printf("npf =%d\n",npf);
    }
    else {
      npf=0;
//      printf("npf =%d\n",npf);
    }
}

//---------------------------------------------------------------------------------------
double frn(double xx)
{
   const double par_frn[5] = {0.9344, -0.1824, 0.02625, -0.001654, 3.233e-5};
   double val = par_frn[0]+par_frn[1]*xx+par_frn[2]*xx*xx+par_frn[3]*xx*xx*xx+par_frn[4]*xx*xx*xx*xx;
   if(val<0.) val = 0.0;
   return val;
}
double frp(double xx)
{
   const double par_frp[7] = {0.4314, -0.0622, 0.01126, -0.00104, 3.001e-5, 0.3561, -0.021};
   double junction = 15.; 
   double val;
   if(xx<junction) {
     val = par_frp[0]+par_frp[1]*xx+par_frp[2]*xx*xx+par_frp[3]*xx*xx*xx+par_frp[4]*xx*xx*xx*xx;
   } else {
     val = par_frp[5]+par_frp[6]*xx;
   }
   if(val<0.) val = 0.0;
   return val;
}

