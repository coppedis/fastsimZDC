double par_t1t2[4] = {0,};
double par_t1t2t3[4] = {0,};
double phetot = 0;
double eneres1 = 0;
double eneres2 = 0;
double showmax = 0;
double par_uni[4] = {0,};

static const double towersize = 0.056; 
static const double LambdaZP = 18.4; // ZP interaction length in cm
static const double Chi0ZP = 1.50; // ZP radiation length in cm
static const double xpitch = 0.4; // fibre spacing along x coord. 
static const double ypitch = 0.4; // fibre spacing along y coord.
static const double thr_dtofib = 0.26;

// ====== parametes for protons ========================
// mean number of phe as a function of GEANT3 released proton energy 
// from ~/fmc/truesidea/ptot.root (pol2 fit)
const double phe_ene_zp[3] = { -1.78, 0.85, 3.5e-06 }; 
// mean number of phe as a function of proton energy when protons interact
// in the beam pipe cone from p0-5000_fromip_cor.root (pol1 fit)
const double phe_ene_int[2] = {-1.68, 0.857};
//  correction to take into account the GEANT3 released energy 
//  and the shower leakage at the edges from simulation file ~/fmc/truesidea/pzp_flat.root with |y|<2.0 cm
double fromEtoG3ene_h = 0.61;
double notunif,signuni; 
// non uniform response of ZP for hadronic ptl; function is (1. +/- expo)
// const double disunif_h[2] = {-2.51,  -11.2};
// non uniform response of ZP for hadronic ptl; function is pol2 from ~/fmc/truesidea/ptot.root
const double disunif_h[3] = { 0.048, 0.1157, 1.271};
 
// ====== parametes for em particles ========================
// to go from energy to G3 energy for em ptl 
// from egzp0-4000_flat.root 
double fromEtoG3ene_em = 0.808;
// mean number of phe as a function of Geant3 released energy 
